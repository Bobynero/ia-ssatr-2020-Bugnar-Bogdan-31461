    private void buttonTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTransferActionPerformed
        String fromOwnerName = this.fromOwnerName.getText();
        String toOwnerName = this.toOwnerName.getText();
        int amount = Integer.parseInt(this.amount.getText());
        if(am.transfer(fromOwnerName, toOwnerName, amount)) this.jTextArea1.append("Transfer successfully made.\n");
        else this.jTextArea1.append("The transfer has failed");
    }//GEN-LAST:event_buttonTransferActionPerformed

